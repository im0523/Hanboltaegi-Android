package com.csslect.app.dto;

import java.sql.Date;

public class ScrapDTO {

	private int scr_id;
	private String scr_userid, scr_recipeid, target_userid;
	private Date scr_date;
	
	public ScrapDTO() {}
	
	public ScrapDTO(int scr_id, String scr_userid, String scr_recipeid, String target_userid, Date scr_date) {
		super();
		this.scr_id = scr_id;
		this.scr_userid = scr_userid;
		this.scr_recipeid = scr_recipeid;
		this.target_userid = target_userid;
		this.scr_date = scr_date;
	}

	public int getScr_id() {
		return scr_id;
	}

	public void setScr_id(int scr_id) {
		this.scr_id = scr_id;
	}

	public String getScr_userid() {
		return scr_userid;
	}

	public void setScr_userid(String scr_userid) {
		this.scr_userid = scr_userid;
	}

	public String getScr_recipeid() {
		return scr_recipeid;
	}

	public void setScr_recipeid(String scr_recipeid) {
		this.scr_recipeid = scr_recipeid;
	}

	public String getTarget_userid() {
		return target_userid;
	}

	public void setTarget_userid(String target_userid) {
		this.target_userid = target_userid;
	}

	public Date getScr_date() {
		return scr_date;
	}

	public void setScr_date(Date scr_date) {
		this.scr_date = scr_date;
	}
	

	
	
	
}
